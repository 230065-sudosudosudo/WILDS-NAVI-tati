import streamlit as st

st.set_page_config(page_title="太刀 — データ更新ガイド", layout="wide")
st.title("データ更新ガイド（太刀）")

st.markdown('''
このサイトは Excel（`太刀.xlsx`）をソースにしています。**最低限必要な列**：
- `武器`, `会心率`, `属性`, `総攻撃力`, `総攻撃力(物理)`, `レア度`

あると便利な列：
- `基礎攻撃力`, `属性値`, `スロット数`, `スキル`, `切れ味` など

## 更新手順
1. 新しい `太刀.xlsx` を作成（上記列名を守る）
2. サイト左サイドバーのファイルアップローダから差し替え
3. 表示が崩れる場合は列名やデータ型（数値/文字列）を点検

## 拡張
- 武器種ごとに Excel を分ける or シートを分ける
- ランキング指標（例: 属性DPS、期待値ダメージ）を追加して列として保存
- 画像URL列を追加 → 詳細ページで表示
''')
